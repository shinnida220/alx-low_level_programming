/**
 * Arrays
 * Arrays in C are contiguous memory areas
 * that hold a number of values of the same type.
 *
 * Unlike some other languages, in C,
 * all elements of an array have the same type.
 *
 * To declare an array we use this syntax:
 * type var_name[number_of_elements];,
 *
 * where number_of_elements is the number of elements
 * of type type that we need.
 */
